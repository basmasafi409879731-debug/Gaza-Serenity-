// زر تغيير النمط
// هذا تعليق يوضح أن الكود التالي مسؤول عن تشغيل زر الـ Dark Mode

$("#modeBtn").click(function () {
    // هنا نستهدف الزر الذي يحمل id="modeBtn"
    // نستخدم jQuery لاختصار كتابة الكود
    // .click(function() { ... }) يعني: عند الضغط على الزر نفّذ ما بداخل الأقواس

    $("body").toggleClass("dark-mode");
    // $("body") يعني نستهدف عنصر <body> كامل الصفحة
    // toggleClass("dark-mode") يعني:
    // إذا كانت كلاس dark-mode موجودة → احذفها
    // إذا كانت غير موجودة → أضفها
    //
    // هذا يجعل الصفحة تنتقل بين الوضع العادي والوضع الداكن
});
